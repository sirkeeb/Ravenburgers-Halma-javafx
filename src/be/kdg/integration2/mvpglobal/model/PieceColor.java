package be.kdg.integration2.mvpglobal.model;

/**
 * The two possible colors of Halma game pieces.
 * <p>
 * BLACK pieces start in the top-left camp and move toward the bottom-right,
 * while WHITE pieces start in the bottom-right camp and move toward the top-left.
 * </p>
 */
public enum PieceColor {
    /** A black-colored piece. */
    BLACK,
    /** A white-colored piece. */
    WHITE
}
